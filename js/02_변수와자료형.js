/* 변수 선언 */
// 메모리에 값을 저장할 공간을 만들고 이름을 붙이는 것
// 작성법 :   변수종류 변수명;
var number1;

/* 변수에 값 대입 */
// 선언된 변수에 값을 대입하는 것
// 작성법 : 변수명 = 값;

/* 대입 연산자(=)
A = B; -> B라는 값을 A변수에 대입 (오른쪽 값을 왼쪽 변수에 대입)

같다를 뜻하는 연산자 (==, 비교 연산자)  */

number1 = 10;

/* 콘솔에 number1 값 출력하기 */
console.log(number1);

/* 변수 선언 + 대입 */
var number2 = 20;
console.log(number2);

/* number1, number2의 합 출력하기 */
console.log(number1 + number2); // 30

/* 변수에 대입한 값 변경하기 */

number1 = 300; // number1에 300 대입(기존 값 10 덮어쓰기)
number2 = 400;

/* 변경된 두 변수의 값 출력 */
/* 1) 문자열 + 문자열/숫자/변수 == 이어쓰기 */
console.log( "number1의 값 : " + number1);
console.log( "number2의 값 : " + number2);

/* 2) 괄호 내부에 , 를 작성해 각각의 값을 독립적으로 출력하기 */
console.log("number1 : ", number1, " / number 2 ", number2);

